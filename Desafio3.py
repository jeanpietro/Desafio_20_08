# Declaração de variável
num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

# Realização das quatro operações aritméticas
soma = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2

# Imprime os resultados das quatro operações
print(f'A soma dos números é igual a: {soma}')
print(f'A subtração dos números é igual a: {sub}')
print(f'A multiplicação dos números é igual a: {mult}')
print(f'A divisão nos números é igual a: {div}')
